public class JavaOperator {
    public static void main(String[] args) {
        int myIntVal = 10;
        System.out.println(myIntVal); //initialize to 10;
        myIntVal +=5;
        System.out.println(myIntVal);//increment to 15;
        myIntVal -=5;
        System.out.println(myIntVal);//decrement to 10;



    }
}
