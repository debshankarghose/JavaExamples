public class TypeManualCasting {
    public static void main(String[] args){
        double d = 9.78d;
        int i = (int) d; //here, we have to do manual casting by passing (int) infront of myDouble indetifier.
        System.out.println("Value of double is: "+d);
        System.out.println("Value of int is: "+i);
    }
}
