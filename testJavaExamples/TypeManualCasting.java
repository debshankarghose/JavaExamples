public class TypeManualCasting {
    public static void main(String[] args){
        double mydouble = 9.78d;
        int myInt = (int) mydouble; //here, we have to do manual casting by passing (int) infront of myDouble indetifier.

        System.out.println("Value of mydouble is: "+mydouble);
        System.out.println("Value of myInt is: "+myInt);


    }
}
