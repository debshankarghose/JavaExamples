public class TypeAutoCasting {
    public static void main(String[] args) {
        int myInt =9;
        double myDouble = myInt; // note: casting from int to double dataType is done automatically in Wide Casting.
         System.out.println("Value of: " +myInt); //output will be int format
        System.out.println("Value of: " +myDouble); //output will be double format
    }
}
